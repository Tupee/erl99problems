%% =========================================================================================
%% @doc
%% P01 (*) Найти последний элемент списка:
%% Пример:
%% 1> p01:last([a,b,c,d,e,f]).
%% f
%% @end
%% =========================================================================================
-module(p01).

-include_lib("eunit/include/eunit.hrl").

-export([last/1]).

last([H|[]])	->	H;
last([_H|T])	-> last(T).

% ======================================== test suite ======================================

last_char_test() -> 
	f = last([a,b,c,d,e,f]).
last_num_test() -> 
	5 = last([1,2,3,4,5]).

